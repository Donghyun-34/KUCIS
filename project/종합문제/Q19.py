import re

phone_num = """
park 010-9999-9988
kim 010-9909-7789
lee 010-8789-7768
"""

phone_pattern = re.compile(r"(\d{3}[-]\d{4})[-]\d{4}")
result = phone_pattern.sub("\g<1>-####", phone_num)

print(result)
"""
분석
1. 정규 표현식 객체를 만들 때 전화번호에서 앞의 7자리를 ()로 묶었고, 나머지 뒤의 4자리를 구분했다.
2. 즉 앞의 7자리가 g<0>, 뒤의 4자리가 g<1>이 된다.
3. 그렇기 때문에 sub에서 뒤의 4자리만 가리고 싶다면 위의 방법으로 하면 된다.
"""